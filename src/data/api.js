import { domain, ssl } from '../config'

// REST calls to the conductor. Creation, begin-after-planning, run commands,
// and planning questions all come from the backend — nothing is hardcoded here.
const base = `${ssl ? 'https' : 'http'}://${domain}/api`

const post = async (path, body) => {
    const res = await fetch(base + path, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: body ? JSON.stringify(body) : undefined,
    })
    if (!res.ok) {
        // Surface the server's reason (e.g. "folder is not readable + writable: …")
        // so the UI can tell the user what to fix, not just a status code.
        const detail = (await res.text().catch(() => '')).trim()
        throw new Error(detail || `${path}: ${res.status}`)
    }
    const text = await res.text()
    return text ? JSON.parse(text) : null
}

// Create a run from the wizard; returns { id }.
export const createRun = (spec) => post('/runs', spec)

// Begin the build once planning is done; the planning answers refine the prompt.
export const beginRun = (id, answers) => post(`/runs/${id}/begin`, { answers })

// Stop | restart.
export const commandRun = (id, command) => post(`/runs/${id}/command`, { command })

// Cancel: abandon a run (works while still in the planning Q&A, where stop has no
// executor to reach). The run is kept as "cancelled" in the Tasks history.
export const cancelRun = (id) => post(`/runs/${id}/cancel`)

// Archive: clear a run from the dashboard, keeping it in the Tasks history.
export const archiveRun = (id) => post(`/runs/${id}/archive`)

// Edit: change a finished run's task in place and reset it to planning (the
// questions regenerate from the new prompt). Distinct from restart (re-run as-is).
export const editRun = (id, spec) => post(`/runs/${id}/edit`, spec)

// System info: platform, dependency state (claude/git/gh), recommendations.
// Doubles as the backend reachability probe.
export const fetchSystem = async () => {
    const res = await fetch(`${base}/system`)
    if (!res.ok) throw new Error(`system: ${res.status}`)
    return res.json()
}

// Workspace CRUD (named folder sets).
export const createWorkspace = (ws) => post('/workspaces', ws)

// Soft-delete a workspace. Resolves on 204. On 409 the workspace is still
// referenced by active runs: throws an Error whose .blocking is the list of
// [{id,title,status}] runs the caller must cancel first (then retry).
export const deleteWorkspace = async (id) => {
    const res = await fetch(`${base}/workspaces/${id}`, { method: 'DELETE' })
    if (res.status === 409) {
        const body = await res.json().catch(() => ({}))
        const err = new Error('Workspace is in use by an active task.')
        err.blocking = Array.isArray(body.blocking) ? body.blocking : []
        throw err
    }
    if (!res.ok) throw new Error(`delete workspace: ${res.status}`)
}

// Planning questions for a run — generated by the backend from the run's own
// prompt (returns [] when it goes straight to the build).
export const fetchQuestions = async (id) => {
    const res = await fetch(`${base}/runs/${id}/questions`)
    if (!res.ok) throw new Error(`questions: ${res.status}`)
    return res.json()
}

// Filesystem (the backend's own): browse directories for the workspace picker,
// and check whether a folder is still a readable+writable directory.
export const listDir = async (path = '') => {
    const res = await fetch(`${base}/fs?path=${encodeURIComponent(path)}`)
    if (!res.ok) throw new Error((await res.text().catch(() => '')).trim() || `fs: ${res.status}`)
    return res.json()
}
export const checkPath = async (path) => {
    const res = await fetch(`${base}/fs/check?path=${encodeURIComponent(path)}`)
    if (!res.ok) throw new Error(`fs check: ${res.status}`)
    return res.json()
}
